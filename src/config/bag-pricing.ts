/**
 * 쇼핑백 category_code 면 추출 매트릭스를 SwadpiaPrintEntry[] 로 합성해 반환.
 * 기존 lookupSwadpiaCost / ProductConfigurator 경로(paper_code+quantity 키)가 그대로 동작하도록
 * 모든 행에 추출 당시의 기본 paper_code 를 부여한다(선택 용지가 매트릭스에 없으면 이 값으로 폴백).
 * 쇼핑백이 아니면 null → 호출측이 라이브 json_data 결과를 그대로 사용.
 */
export function synthesizeBagPrintEntries(categoryCode: string): SwadpiaPrintEntry[] | null {
  const m = BAG_PRICE_MATRIX[categoryCode]
  if (!m) return null
  return m.matrix.map((r) => ({
    quantity: r.quantity,
    paper_code: m.defaultPaperCode,
    print_method: '',
    print_unit1: r.cost_krw,
    print_unit2: r.cost_krw,
    add_unit2: 0,
  }))
}
